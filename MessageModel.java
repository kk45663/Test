public class MessageModel {
    private String messageTitle;
    private String messageDesc;
    private String messageDate;
    private String messageLinkType;
    private String messageTypeName;
    private String messageButtonName;

    public void setMessageTitle(String messageTitle) {
        this.messageTitle = messageTitle;
    }

    public String getMessageTitle() {
        return messageTitle;
    }

    public void setMessageDesc(String messageDesc) {
        this.messageDesc = messageDesc;
    }

    public String getMessageDesc() {
        return messageDesc;
    }

    public void setMessageDate(String messageDate) {
        this.messageDate = messageDate;
    }

    public String getMessageDate() {
        return messageDate;
    }

    public void setMessageLinkType(String messageLinkType) {
        this.messageLinkType = messageLinkType;
    }

    public String getMessageLinkType() {
        return messageLinkType;
    }

    public void setMessageTypeName(String messageTypeName) {
        this.messageTypeName = messageTypeName;
    }

    public String getMessageTypeName() {
        return messageTypeName;
    }

    public void setMessageButtonName(String messageButtonName) {
        this.messageButtonName = messageButtonName;
    }

    public String getMessageButtonName() {
        return messageButtonName;
    }
}
